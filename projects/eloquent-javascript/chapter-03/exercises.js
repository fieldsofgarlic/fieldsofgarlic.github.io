////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min( n1, n2 ) {
  return [ n1, n2 ].sort( ( a, b ) => a - b )[ 0 ];
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven( n ) {
    switch ( Math.abs( n ) ) {
        case 0: return true;
        case 1: return false;
        default: return isEven( n - 2 );
    }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars( str, char ) {
  return [ ...str ].filter( c => c === char ).length;
}

function countChar( str, char ) {
  return [ ...str ].filter( c => c === char ).length;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs( str ) {
  return countChar( str, 'B' );
}

