function triangles( size ) {
  for (let i = 0; i < size; i++) {
    console.log('#'.repeat(i + 1));
  }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz(start, end) {
  for (let i = start; i <= end; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
      console.log('fizzbuzz');
    }
    else if (i % 3 === 0) {
      console.log('fizz');
    }
    else if (i % 5 === 0) {
      console.log('buzz');
    }
    else {
      console.log(i);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(bindingSize) {
  bindingSize = bindingSize || 8;
  let output = '';
  for (let i = 0; i < bindingSize; i++) {
    let row = '';
    let order = i % 2 > 0 ? [ '#', ' ' ] : [ ' ', '#' ];
    for (var j = 0; j < bindingSize; j++) {
      row += order[ j % 2 ];
    }
    output += `${row}\n`;
  }
  console.log(output);
}

